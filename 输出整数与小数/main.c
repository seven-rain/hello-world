#include <stdio.h>
#include <stdlib.h>
//输入一个数，输出其整数部分与小鼠部分
int main()
{
    float m;//定义m的变量类型，为浮点型
    int n;//定义n的变量类型，为整形
    scanf("%f",&m);//用键盘输入m的值
    n = (int)(m);//用强制转换语句将m从浮点形转换为整型，并把得到的整数值赋给n，此时n的值就是m的整数部分
    m = m - n;//用m减去其整数部分剩下的就是小数部分
    printf("整数部分=%d\n",n);//输出整数部分的值
    printf("小数部分=%1.2f\n",m);//输出小数部分的值，1.2的定义是一个字节，两位小鼠
    return 0;
}
